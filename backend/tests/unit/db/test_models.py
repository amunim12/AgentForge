# User model defaults
# ---------------------------------------------------------------------------
class TestUserModel:
    def test_id_is_uuid_string(self) -> None:
        user = User(
            email="a@b.com",
            username="a",
            hashed_password="hash",
        )
        # id is generated by default_factory
        assert isinstance(user.id, str)
        parsed = uuid.UUID(user.id)
        assert str(parsed) == user.id

    def test_two_users_get_different_ids(self) -> None:
        u1 = User(email="a@b.com", username="a", hashed_password="h")
        u2 = User(email="b@c.com", username="b", hashed_password="h")
        assert u1.id != u2.id

    def test_is_active_defaults_to_true(self) -> None:
        user = User(email="x@y.com", username="x", hashed_password="h")
        assert user.is_active is True

    def test_created_at_is_utc_datetime(self) -> None:
        user = User(email="t@u.com", username="t", hashed_password="h")
        assert isinstance(user.created_at, datetime)
        assert user.created_at.tzinfo is not None

    def test_tasks_relationship_starts_empty(self) -> None:
        user = User(email="r@s.com", username="r", hashed_password="h")
        # Relationship list is not loaded until DB access; in-memory it's empty list.
        assert user.tasks == []


