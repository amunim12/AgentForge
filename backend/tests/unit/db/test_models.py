# Enum value tests
# ---------------------------------------------------------------------------
class TestTaskStatusEnum:
    def test_values_are_strings(self) -> None:
        assert TaskStatus.PENDING == "pending"
        assert TaskStatus.PLANNING == "planning"
        assert TaskStatus.EXECUTING == "executing"
        assert TaskStatus.CRITIQUING == "critiquing"
        assert TaskStatus.COMPLETED == "completed"
        assert TaskStatus.FAILED == "failed"

    def test_usable_in_string_context(self) -> None:
        # StrEnum members compare equal to their string value directly.
        assert f"{TaskStatus.PENDING}" == "pending"
        assert f"status={TaskStatus.COMPLETED}" == "status=completed"

    def test_all_members_present(self) -> None:
        members = {s.value for s in TaskStatus}
        assert members == {"pending", "planning", "executing", "critiquing", "completed", "failed"}


